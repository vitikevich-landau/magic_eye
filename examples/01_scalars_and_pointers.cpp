// ============================================================================
//  Пример 01: основы — скаляры, enum, указатели, осмотр типа без объекта
//  Сборка: g++ -std=c++20 -Wall -Wextra -Wpedantic -I../include 01_*.cpp -o ex01
// ============================================================================
#include <eye/magic_eye.hpp>

#include <cstdint>

enum class School : std::uint16_t { Air = 1, Fire = 2, Earth = 4, Water = 8 };

int main() {
    // ТЕРМИНАЛ → интерактивный обозреватель: ↑↓ ходят по корням, →/Enter
    // раскрывает узел и ПЕРЕХОДИТ по указателю, ← сворачивает, 1..6 — прыжок
    // к корню, ? — помощь, q — выход. В пайп/файл (./ex_01 | less) — те же
    // панели статикой, сверху вниз. Один объект — короче: eye::explore(x).
    int gold = 0x11223344;      // скаляр: в дампе виден little-endian
    std::uint8_t level = 200;   // узкий тип — Око подпишет hex рядом
    double luck = 1.0;
    School spell = School::Fire;   // enum class поверх uint16_t
    int* ptr = &gold;           // указатель: в терминале Enter → перейдёт к казне

    // Каждый .add — корень галереи; объекты живут до конца run() (галерея
    // держит на них ссылку, поэтому — именованные локальные, не временные).
    eye::Gallery{}
        .add(gold,  "казна (int)")
        .add(level, "уровень (uint8_t)")
        .add(luck,  "удача (double)")
        .add(spell, "школа магии (enum class)")
        .add(ptr,   "указатель на казну")   // Enter на нём ⟲ прыгнет к «казне»
        .add<double>()                       // осмотр ТИПА без объекта
        .run();
}
